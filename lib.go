package math_module

// package math_module provides a single utility to calculate the value to two numbers.

// Add calculates the value of summing up two numbers.
//
// It has two parameters which are instances of int literal. Add returns the summation of the two int
// parameters passed in.
//
// More information on math can be found at [Math is fun].
// [Math is fun]: https://www.mathsisfun.com/numbers/addition.html
func Add(a, b int) int {
	return a + b
}
